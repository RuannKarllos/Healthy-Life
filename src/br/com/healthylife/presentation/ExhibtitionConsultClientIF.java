    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        try {
        ControllerConsultation cc = new ControllerConsultation();

        String id = jTextField1.getText();
        String folder = "Consultation";

        File file = new File(folder, id + ".data");

        if (file.exists()) {
            Consultation c = cc.search(id + ".data");
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            jLabelHospital.setText(c.getHospitalName());
            jLabelDate.setText(format.format(c.getConsultationDate()));
        } else {
            throw new NullPointerException("Consulta n√£o encontrada!");
        }
        }catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed

